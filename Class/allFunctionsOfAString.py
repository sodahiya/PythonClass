#function/method used on string
#take a variable
a="--This is a random String--"
#by using len() function to find length of string
print(len(a))
#using strip() function
print(a.strip('-'))
#using rstrip() function
print(a.rstrip('-'))
#using lstrip() function
print(a.lstrip('-'))
#find() function used for finding something/feturn index number
print(a.find('a'))
#rfind() function//find last occurance
print(a.rfind('a'))
#index() function
print(a.index('a'))
#rindex() function
print(a.rindex('a'))
#count function is used
print(a.count('a'))
#upper() function
print(a.upper())
#lower() function
print(a.lower())
#capitalize() function
print(a.capitalize())
#startwith() function
print(a.startswith('-'))
#endwith() function
print(a.endswith('-'))
#replace() function
print(a.replace("kumar","singh"))
#slpit() function
print(a.split())
#title() function
print(a.title())